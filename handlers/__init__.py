# handlers/__init__.py
from . import auth, admin, user

def register_all_handlers(dp):
    """Регистрация всех обработчиков в правильном порядке"""
    # Порядок регистрации ОЧЕНЬ важен!
    
    # 1. Сначала авторизация (самый высокий приоритет)
    auth.setup(dp)
    
    # 2. Затем админские обработчики (более специфические)
    admin.setup(dp)
    
    # 3. В конце пользовательские обработчики (включая catch-all для кастомных кнопок)
    user.setup(dp)